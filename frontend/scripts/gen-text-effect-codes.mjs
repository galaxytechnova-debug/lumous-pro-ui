import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const uiDir = path.join(root, "components", "ui");

const files = [
  ["explosive-letter-burst.tsx", "explosiveLetterBurstCode"],
  ["shadow-dance-lumous.tsx", "shadowDanceLumousCode"],
  ["melting-text-lumous.tsx", "meltingTextLumousCode"],
  ["matrix-lumous.tsx", "matrixLumousCode"],
  ["spin-3d-lumous.tsx", "spin3DLumousCode"],
  ["split-text-lumous.tsx", "splitTextLumousCode"],
  ["glow-3d-lumous.tsx", "glow3DLumousCode"],
];

let out =
  "// Auto-generated by scripts/gen-text-effect-codes.mjs — run: npm run gen:text-codes\n\n";

for (const [filename, exportName] of files) {
  const content = fs.readFileSync(path.join(uiDir, filename), "utf8");
  out += `export const ${exportName} = ${JSON.stringify(content)};\n\n`;
}

const outPath = path.join(root, "lib", "lumous-text-effect-codes.ts");
fs.writeFileSync(outPath, out);
console.log("Wrote", outPath);
